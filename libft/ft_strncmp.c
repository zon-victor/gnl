#include "libft.h"

int		ft_strncmp(char *s1, char *s2, int n)
{
	int i;

	i = 0;
	while (*s1 == *s2 && i < n)
	{
		if (i == n - 1)
		{
			return (0);
		}
		i++;
		s1++;
		s2++;
	}
	return (*s1 - *s2);
}
